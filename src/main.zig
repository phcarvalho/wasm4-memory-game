var prnd = std.Random.DefaultPrng.init(42);

const CARD_SIZE: u8 = 24;
const SIDE_PANEL_POS: u8 = 112;

const Card = struct {
    id: u4,
    sprite: *const [144]u8,
    found: bool = false,
};

const cards: [8]Card = .{
    Card{ .id = 1, .sprite = &sprites.card_front_1, .found = false },
